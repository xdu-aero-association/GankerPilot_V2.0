/***************************主要核心：传感器滤波、姿态解算、惯导、控制等代码在Time_Cnt.c里面运行**********************************/
int main()
{
  HardWave_Init();																			//硬件资源+相关外设初始化
  delay_ms(500);	
  while(1)
  {
		Get_Battery_Voltage();															//测量电池电压
		Key_Scan(Key_Right_Release);												//按键扫描
		QuadShow(Oled_Show_Enable);													//OLED显示，飞控两个按键同时按下，停止/恢复自动刷新
		Accel_Calibartion();																//加速度计标定
		Mag_Calibartion_LS(&WP_Sensor.mag_raw,Circle_Angle);//磁力计椭球校准
		RC_Calibration_Check(PPM_Databuf);									//遥控器行程标定	
		Save_Or_Reset_PID_Parameter();											//运用地面站，修改控制参数 
  }
}
